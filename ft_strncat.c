#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*a;
	size_t	b;

	if (n == 0)
		return (s1);
	a = s1;
	b = 0;
	while (*a != '\0')
		a++;
	while ((*a = *s2))
	{
		a++;
		s2++;
		if (b >= (n - 1))
			break ;
		b++;
	}
	*a = '\0';
	return (s1);
}
